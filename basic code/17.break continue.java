public class Break{
    public static void main(String[] args) {
        //break statement
        int i,j;
        for(i=1;i<=100;i++){
            if(i==10)
                break;
            
        System.out.println(i);
    }
    //continue statement
    for(j=1;j<=11;j++){
    if(j==5)
        continue;
    System.out.println(j);
    }
}
  
}
